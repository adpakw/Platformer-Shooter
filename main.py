class Player(pygame.sprite.Sprite):
    def __init__(self, x, y, scale):
        pygame.sprite.Sprite.__init__(self)
        img = pygame.image.load('img/player/Idle/0.png')
        self.image = pygame.transform.scale(img, (int(img.get_width() * scale), int(img.get_height() * scale)))
        self.rect = self.image.get_rect()
        self.rect.center = (x, y)

    def draw(self):
        screen.blit(self.image, self.rect)

player = Player(200, 200, 1)
player2 = Player(400, 200, 2)


while True:
    screen.blit(background_img, (0, 0))  # draw an image (рисуем изображение)

    player.draw()
    player2.draw()
    clock.tick(FPS)
    draw_grid()

    pygame.display.update()  # update portions of the screen for software displays (обновляем наше окно)
    for event in pygame.event.get():
        # quit game (выйти из игры)
        if event.type == pygame.QUIT:
            pygame.quit()
        if event.type == pygame.KEYDOWN:
            if event.key == pygame.K_ESCAPE:
                pygame.quit()
